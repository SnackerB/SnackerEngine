#include "Gui/GuiEventHandles/GuiEventHandle2.h"

namespace SnackerEngine
{
	//--------------------------------------------------------------------------------------------------
	void GuiEventHandle2::activate()
	{
		active = true;
		onEvent();
	}
	//--------------------------------------------------------------------------------------------------
	GuiEventHandle2::GuiEventHandle2()
		: GuiHandle2(), active(false) {}
	//--------------------------------------------------------------------------------------------------
	bool GuiEventHandle2::isActive() const
	{
		return active;
	}
	//--------------------------------------------------------------------------------------------------
	void GuiEventHandle2::reset()
	{
		active = false;
	}
	//--------------------------------------------------------------------------------------------------
}
