#include "GuiEventHandle.h"

namespace SnackerEngine
{
	//--------------------------------------------------------------------------------------------------
	void GuiEventHandle::activate()
	{
		active = true;
		onEvent();
	}
	//--------------------------------------------------------------------------------------------------
	GuiEventHandle::GuiEventHandle()
		: GuiHandle(), active(false) {}
	//--------------------------------------------------------------------------------------------------
	bool GuiEventHandle::isActive() const
	{
		return active;
	}
	//--------------------------------------------------------------------------------------------------
	void GuiEventHandle::reset()
	{
		active = false;
	}
	//--------------------------------------------------------------------------------------------------
}
